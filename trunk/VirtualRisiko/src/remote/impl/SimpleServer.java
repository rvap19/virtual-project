    public static final String AUTHENTICATE="authenticate";
    public static final String HELLO="hello";
    public static final String REGISTRATION="registration";
    public static final String ISONLINE="isonline";
    public static final String AVAILABLEGAMES="availablegames";
    public static final String DELETE_REGISTRATION="deleteRegistration";
    public static final String DELETE_GAME="deleteGame";
    public static final String CHANGE_MANAGER="changeManager";
    public static final String SAVE_RESULTS="saveResult";
    public static final String CREATE_GAME="createGame";
    public static final String GET_AUTHENTICATES="getAuthenticateUsers";
    public static final String PLAYERS_IN_GAME="getPlayers";
    public static final String GAME_FORID="getGame";
    public static final String REGISTRATION_FOR_PLAYER="getActiveRegistrationGame";
    public static final String ADDRESS="address";
  
    
   // Default locator values
    private Risiko risikoService;
    private HashMap<String,InetAddress> address;
   private static String transport = "socket";
   private static String host = "192.168.1.114";
   public static int port = 5400;

   // String to be returned from invocation handler upon client invocation calls.
   private static final String RESPONSE_VALUE = "This is the return to SampleInvocationHandler invocation";


   public void setupServer(String locatorURI) throws Exception
   {
      // create the InvokerLocator based on url string format
      // to indicate the transport, host, and port to use for the
      // server invoker.
       this.risikoService=new RisikoService();
       address=new HashMap<String,InetAddress>();
      InvokerLocator locator = new InvokerLocator(locatorURI);
      System.out.println("Starting remoting server with locator uri of: " + locatorURI);
      Connector connector = new Connector(locator);
      // creates all the connector's needed resources, such as the server invoker
      connector.create();

      // create the handler to receive the invocation request from the client for processing
     
      
      // first parameter is sub-system name.  can be any String value.
      connector.addInvocationHandler(HELLO, new SampleInvocationHandler());
      connector.addInvocationHandler(AUTHENTICATE, new AuthenticationInvocationHandler()); 
      connector.addInvocationHandler(AVAILABLEGAMES , new AvailableGamesInvocationHandler()); 
      connector.addInvocationHandler(CHANGE_MANAGER , new ChangeManagerInvocationHandler());
      connector.addInvocationHandler(CREATE_GAME , new CreateGameInvocationHandler());
      connector.addInvocationHandler(DELETE_GAME , new DeleteGameInvocationHandler());
      connector.addInvocationHandler(DELETE_REGISTRATION , new DeleteRegistrationInvocationHandler());
      connector.addInvocationHandler(GAME_FORID , new GameForIDInvocationHandler());
      connector.addInvocationHandler(GET_AUTHENTICATES , new AuthenticateUsersInvocationHandler()); 
      connector.addInvocationHandler(ISONLINE , new IsOnLineInvocationHandler());
      connector.addInvocationHandler(PLAYERS_IN_GAME , new PlayersInGameInvocationHandler());
      connector.addInvocationHandler(REGISTRATION , new RegistrationInvocationHandler());
      connector.addInvocationHandler(REGISTRATION_FOR_PLAYER , new ActiveRegistrationInvocationHandler());
      connector.addInvocationHandler(SAVE_RESULTS , new SaveResultsInvocationHandler()); 
      connector.addInvocationHandler(ADDRESS  , new AddressInvocationHandler()); 
      // start with a new non daemon thread so
      // server will wait for request and not exit
      connector.start();

   }

   /**
    * Can pass transport and port to be used as parameters.
    * Valid transports are 'rmi' and 'socket'.
    *
    * @param args
    */
   public static void main(String[] args)
   {
      if(args != null && args.length == 3)
      {
         transport = args[0];
         host = args[1];
         port = Integer.parseInt(args[2]);
      }
      String locatorURI = transport + "://" + host + ":" + port;
      SimpleServer server = new SimpleServer();
      try
      {
         server.setupServer(locatorURI);

         // wait forever, let the user kill us at any point (at which point, the client will detect we went down)
         while(true)
         {
            Thread.sleep(1000);
         }

      }
      catch(Exception e)
      {
         e.printStackTrace();
      }
   }

