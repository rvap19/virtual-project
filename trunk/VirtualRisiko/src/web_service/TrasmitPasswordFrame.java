    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username=this.usernameText.getText();
        if(username==null || username.trim().equals("")){
            JOptionPane.showMessageDialog(this, "inserire username", "Virtual Risiko info", JOptionPane.ERROR_MESSAGE);   
            return;
        }
        
        if(client.checkUsername(username)){
            JOptionPane.showMessageDialog(this, "username inesistente", "Virtual Risiko info", JOptionPane.ERROR_MESSAGE);      
        }else if(client.sendEmailForPassword(username)){
            JOptionPane.showMessageDialog(this, "La tua password e' stata inviata all'indirizzo specificato", "Virtual Risiko info", JOptionPane.INFORMATION_MESSAGE);      
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "errore durante l'invio dell'email", "Virtual Risiko info", JOptionPane.ERROR_MESSAGE);      
        
        }
        
           
    }//GEN-LAST:event_jButton1ActionPerformed

