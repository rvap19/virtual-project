    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username=this.usernameText.getText();
        String codice=this.codiceArea.getText();


        if(username==null||username.equals("")){
            this.messageLabel.setText("inserire username");
            return;
        }

        if(codice==null||codice.equals("")){
            this.messageLabel.setText("inserire codice");
            return;
        }

        int value=-1;
        try{
            value=Integer.parseInt(codice);
        }catch(Exception e){

        }

        if(value==-1){
            this.messageLabel.setText("codice non valido");
            return;
        }

        if(server.activateUser(username, value)){
            this.messageLabel.setText("utente confermato...adesso fai il login");
            try {
                Thread.sleep(1500);
            } catch (InterruptedException ex) {
                Logger.getLogger(AccountActivFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }else{
            this.messageLabel.setText("dati non validi");
        }

        
        
    }//GEN-LAST:event_jButton1ActionPerformed

